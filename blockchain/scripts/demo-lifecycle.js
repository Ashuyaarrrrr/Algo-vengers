  console.log(`  Actor: ${bold("Vedic Wellness")} → ${bold("PharmaLink Distributors, Delhi")}`);
  console.log();

  const t4Receipt = await sendTx(
    "transferOwnership() — shipping finished product",
    provenance.connect(manufAcc).transferOwnership(
      batchId,
      distAcc.address,
      "PharmaLink Distribution Hub, Okhla Phase-II, New Delhi",
      "BoL #VW-DL-2026-1847. 5,000 bottles AshwaGold 500mg. Temperature-controlled logistics.",
      "ipfs://QmBillOfLading-VW-2026-1847"
    )
  );
  const t4Event = parseEvent(provenance, t4Receipt, "OwnershipTransferred");
  txLine("Product shipped to Distributor!", t4Receipt.hash, t4Receipt.gasUsed);
  ok("From", `${shortAddr(t4Event.args.from)} (Manufacturer)`);
