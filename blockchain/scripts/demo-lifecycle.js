  console.log(`  ${bold("🌿  HerbChain — Full Supply Chain Demo")}`);
  console.log(`  ${gray("Ashwagandha Root Batch: Farm to Shelf")}`);
  console.log(green(WALL));
  console.log();
  console.log(`  ${bold("Network:")}   ${cyan(hre.network.name)}`);
  console.log(`  ${bold("Admin:")}     ${shortAddr(deployer.address)}`);
  console.log(`  ${bold("Collector:")} ${shortAddr(collectorAcc.address)}`);
  console.log(`  ${bold("Processor:")} ${shortAddr(processorAcc.address)}`);
  console.log(`  ${bold("Lab:")}       ${shortAddr(labAcc.address)}`);
  console.log(`  ${bold("Manuf.:")}    ${shortAddr(manufAcc.address)}`);
  console.log(`  ${bold("Distrib.:")}  ${shortAddr(distAcc.address)}`);
