  console.log(`  Actor: ${bold("Consumer")} (anyone — read-only, no gas needed)`);
  console.log();

  const prov = await provenance.viewBatchProvenance(batchId);

  section("📋 Product Identity");
  ok("Herb Name",     prov.herbName);
  ok("Batch ID",      shortId(prov.batchId));
  ok("Collector",     shortAddr(prov.collector));
  ok("Sustainable",   prov.isSustainable ? green("✓ Yes") : "No");
  ok("QA Status",     prov.hasPassedLatestQualityTest ? green("✅ Passed") : red("❌ Failed"));
  ok("Current Holder",shortAddr(prov.currentOwner));

  section("📍 Harvest Location");
  ok("GPS Lat",  prov.collectionEvent.gpsLatitude + "°N");
  ok("GPS Long", prov.collectionEvent.gpsLongitude + "°E");
  ok("Weather",  prov.collectionEvent.weather);
  ok("Temp",     prov.collectionEvent.temperature.toString() + "°C");
  ok("Species",  prov.collectionEvent.herbSpecies);
  ok("Quantity", (Number(prov.collectionEvent.quantity) / 1000).toFixed(1) + " kg");

  section("⚙️ Processing Steps (" + prov.processingSteps.length + ")");
  const procNames = ["Drying","Grinding","Storage","Extraction","Sorting","Packaging"];
  prov.processingSteps.forEach((s, i) => {
    ok(`Step ${i+1}`, `${procNames[Number(s.processType)]} @ facility ${s.facilityId}`);
  });

  section("🔬 Quality Tests (" + prov.qualityTests.length + ")");
  prov.qualityTests.forEach((t, i) => {
    ok(`Test ${i+1} Lab`,      shortAddr(t.labAddress));
    ok(`Test ${i+1} Moisture`, (Number(t.moistureContent)/100).toFixed(2) + "%");
    ok(`Test ${i+1} Pesticide`,Number(t.pesticideLevel) + " ppb");
    ok(`Test ${i+1} DNA Auth`, t.dnaAuthenticated ? "✓ Authenticated" : "Not Done");
    ok(`Test ${i+1} Result`,   t.passed ? green("✅ PASSED") : red("❌ FAILED"));
  });

  section("🔗 Custody Chain (" + prov.transfers.length + " transfers)");
  const roleNames = ["Collector","Processor","Lab","Manufacturer","Distributor","Retailer"];
  prov.transfers.forEach((t, i) => {
    const dt = new Date(Number(t.timestamp) * 1000).toLocaleTimeString();
    ok(`Transfer ${i+1}`,
      `${shortAddr(t.from)} → ${shortAddr(t.to)}  [${t.location.slice(0,30)}…]`);
  });

  // ════════════════════════════════════════════════════════════════════════════
  // SUMMARY
  // ════════════════════════════════════════════════════════════════════════════

  const FINAL_LINE = "═".repeat(62);
  console.log(`\n${green(FINAL_LINE)}`);
  console.log(`  ${bold("🏆  DEMO COMPLETE — Full Lifecycle Verified")}`);
  console.log(green(FINAL_LINE));
  console.log();
  console.log(`  ${bold("Contract Address:")}  ${cyan(contractAddress)}`);
  console.log(`  ${bold("Batch ID:")}          ${cyan(shortId(batchId))}`);
  console.log(`  ${bold("Formulation ID:")}    ${cyan(shortId(formulationId))}`);
  console.log();
