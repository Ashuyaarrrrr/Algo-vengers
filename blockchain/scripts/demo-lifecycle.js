  console.log(`  Actor: ${bold("PharmaLink")} → ${bold("NaturaLife Wellness Store, Pune")}`);
  console.log();

  const t5Receipt = await sendTx(
    "transferOwnership() — last-mile delivery to retail store",
    provenance.connect(distAcc).transferOwnership(
      batchId,
      retailerAcc.address,
      "NaturaLife Wellness Store #42, Koregaon Park, Pune",
      "Last-mile delivery completed. Invoice #PL-PUN-2026-7391. Cold chain maintained.",
      "ipfs://QmDeliveryReceipt-PL-PUN-2026"
    )
  );
  const t5Event = parseEvent(provenance, t5Receipt, "OwnershipTransferred");
  txLine("Delivered to Retailer!", t5Receipt.hash, t5Receipt.gasUsed);
  ok("From", `${shortAddr(t5Event.args.from)} (Distributor)`);
  ok("To",   `${shortAddr(t5Event.args.to)} (Retailer)`);
