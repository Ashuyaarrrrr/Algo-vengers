  const fReceipt = await sendTx(
    "recordFormulation() — AshwaGold 500mg capsule recipe",
    provenance.connect(manufAcc).recordFormulation(
      FORMULATION.productName,
      FORMULATION.dosage,
      FORMULATION.herbNames,
      FORMULATION.percentages,
      FORMULATION.ipfsHash
    )
  );

  const fEvent = parseEvent(provenance, fReceipt, "FormulationCreated");
  const formulationId = fEvent.args.formulationId;

  txLine("Formulation recorded!", fReceipt.hash, fReceipt.gasUsed);
  ok("Formulation ID", shortId(formulationId));
  ok("Product Name", FORMULATION.productName);
  ok("Dosage", FORMULATION.dosage);
