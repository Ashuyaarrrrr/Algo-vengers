  // STEP 6 — Lab transfers to Manufacturer
  // ════════════════════════════════════════════════════════════════════════════

  banner(6, "Lab → Manufacturer custody transfer", "🔄");
  console.log(`  Actor: ${bold("AyurTest Lab")} → ${bold("Vedic Wellness Pvt. Ltd., Mumbai")}`);
  console.log();

  const t3Receipt = await sendTx(
    "transferOwnership() — QA-approved batch to manufacturer",
    provenance.connect(labAcc).transferOwnership(
      batchId,
      manufAcc.address,
      "Vedic Wellness Manufacturing Plant, Navi Mumbai",
      "QA certificate #AyurTest-2026-ASH-001 enclosed. All tests passed. COA attached.",
      "ipfs://QmCertificateOfAnalysis"
    )
  );
  txLine("Transferred to Manufacturer!", t3Receipt.hash, t3Receipt.gasUsed);
  ok("Current Owner", `${shortAddr(manufAcc.address)} (Manufacturer)`);

  // ════════════════════════════════════════════════════════════════════════════
  // STEP 7 — Manufacturer records product formulation
  // ════════════════════════════════════════════════════════════════════════════

