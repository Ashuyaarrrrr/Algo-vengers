  console.log(`  ${bold("📦  Batch Profile")}`);
  console.log(`  ${yellow("━".repeat(58))}`);
  console.log(`  Herb Species : ${cyan(BATCH_DATA.herbSpecies)}`);
  console.log(`  Herb Name    : ${bold(BATCH_DATA.herbName)}`);
  console.log(`  Quantity     : ${cyan("500 kg")}`);
  console.log(`  Harvest Date : ${new Date(harvestDate * 1000).toDateString()}`);
  console.log(`  Location     : ${BATCH_DATA.geoLatitude}°N, ${BATCH_DATA.geoLongitude}°E  (Madhya Pradesh)`);
  console.log(`  Sustainable  : ${green("Yes ✓")}`);
  console.log(`  Quality      : ${green(BATCH_DATA.initialQuality)}`);

  // ════════════════════════════════════════════════════════════════════════════
  // STEP 1 — Farmer / Collector creates the batch
  // ════════════════════════════════════════════════════════════════════════════

